public class Match {
    String startTime;
    String vsGrade;
    String firstTeam;
    String secondTeam;
    String rank;
    String name;
    String sumMatch;
    String win;
    String equal;
    String lose;
    String sumGrade;


    public void setRank(String rank) {
        this.rank = rank;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSumMatch(String sumMatch) {
        this.sumMatch = sumMatch;
    }

    public void setWin(String win) {
        this.win = win;
    }

    public void setEqual(String equal) {
        this.equal = equal;
    }

    public void setLose(String lose) {
        this.lose = lose;
    }

    public void setSumGrade(String sumGrade) {
        this.sumGrade = sumGrade;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public void setVsGrade(String vsGrade) {
        this.vsGrade = vsGrade;
    }

    public void setFirstTeam(String firstTeam) {
        this.firstTeam = firstTeam;
    }

    public void setSecondTeam(String secondTeam) {
        this.secondTeam = secondTeam;
    }

    public String getStartTime() {
        return startTime;
    }

    public String getVsGrade() {
        return vsGrade;
    }

    public String getFirstTeam() {
        return firstTeam;
    }

    public String getSecondTeam() {
        return secondTeam;
    }

    public String getRank() {
        return rank;
    }

    public String getName() {
        return name;
    }

    public String getSumMatch() {
        return sumMatch;
    }

    public String getWin() {
        return win;
    }

    public String getEqual() {
        return equal;
    }

    public String getLose() {
        return lose;
    }

    public String getSumGrade() {
        return sumGrade;
    }
}
